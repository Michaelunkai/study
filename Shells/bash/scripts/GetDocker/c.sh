fix_dns() {
  if nslookup google.com >/dev/null 2>&1; then return 0; fi
  warn "DNS broken Ã¢â‚¬â€ fixing..."
  # /etc/resolv.conf is a WSL-managed symlink Ã¢â‚¬â€ must unlink first
