# method chaining
# Calling multiple methods in a single line, each 
# affecting the object, enabling concise code.

class car:

    def turn_on(self):
        print("You start the engine")

    def drive(self):
        print("You drive the car")

    def brake(self):
        print("You step on the brakes")

    def turn_off(self):
        print("You turn off the engine")